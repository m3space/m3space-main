
namespace M3Space.GroundControl.Core.DataModel
{
    /// <summary>
    /// Parameter unit definitions.
    /// </summary>
    public class Unit
    {
        /// <summary>
        /// No unit (scalar).
        /// </summary>
        public const string None = "";

        /// <summary>
        /// Bar (pressure).
        /// </summary>
        public const string Bar = "bar";

        /// <summary>
        /// Centigrades (temperature).
        /// </summary>
        public const string Celsius = "°C";

        /// <summary>
        /// Degrees (angle).
        /// </summary>
        public const string Degrees = "°";

        /// <summary>
        /// Meters (distance).
        /// </summary>
        public const string Meters = "m";

        /// <summary>
        /// Meters per second (speed).
        /// </summary>
        public const string MetersPerSecond = "m/s";

        /// <summary>
        /// Percentage (scalar).
        /// </summary>
        public const string Percent = "%";

        /// <summary>
        /// Volt (voltage).
        /// </summary>
        public const string Volt = "V";

    }
}
